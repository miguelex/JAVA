    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        Origen = new AFND();
        ArrayList<Integer> estadosfinales = new ArrayList<>();

        //Insertar estados finales
        String Area = jTextArea1.getText();
        String[] lineas = Area.split("\n");

        for (int i = 0; i < lineas.length; i++) {
            estadosfinales.add(Integer.parseInt(lineas[i]));
        }
        int[] finales = new int[estadosfinales.size()];

        for (int i = 0; i < estadosfinales.size(); i++) {
            finales[i] = estadosfinales.get(i);
        }
        Origen.setEstadosFinales(finales);

        //Insertar transiciones
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            TransicionAFND transicion = new TransicionAFND();
            ArrayList<Integer> ef = new ArrayList<>();
            transicion.setEstadoInicial(Integer.parseInt(jTable1.getValueAt(i, 0).toString()));
            transicion.setCaracter(jTable1.getValueAt(i, 1).toString().charAt(0));
            ef.add(Integer.parseInt(jTable1.getValueAt(i, 2).toString()));
            transicion.setEstadosFinales(ListToInt(ef));
            Origen.getTransiciones().add(transicion);

        }

        //Insertar Transiciones lambda
        for (int i = 0; i < jTable2.getRowCount(); i++) {
            TransicionLambda transicionL = new TransicionLambda();
            ArrayList<Integer> ef2 = new ArrayList<>();
            transicionL.setEstadoInicial(Integer.parseInt(jTable2.getValueAt(i, 0).toString()));
            ef2.add(Integer.parseInt(jTable2.getValueAt(i, 1).toString()));
            transicionL.setEstadosFinales(ListToInt(ef2));
            Origen.getTransicionesLambda().add(transicionL);
        }

        //Automata cargado. Activamos el boton para poder evaluar
        jButton6.setVisible(true);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText(null);
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        //Caso de tener algo cargado anteriormeten, reseteamos la tabla
        if (jTable1.getRowCount() != 0) {
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                modelo.removeRow(i);
                i -= 1;
            }
        }
        DefaultTableModel modelo2 = (DefaultTableModel) jTable2.getModel();
        //Caso de tener algo cargado anteriormeten, reseteamos la tabla
        if (jTable2.getRowCount() != 0) {
            for (int i = 0; i < jTable2.getRowCount(); i++) {
                modelo2.removeRow(i);
                i -= 1;
            }
        }
        jTextField7.setText(null);
        jButton6.setVisible(false);
    }//GEN-LAST:event_jButton5ActionPerformed

