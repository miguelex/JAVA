    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Vamos a insertar una nueva transicion pero solo si los tres campos contienen algo
        if (!jTextField2.getText().isEmpty() && !jTextField3.getText().isEmpty() && !jTextField4.getText().isEmpty()) {
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            Object[] fila = new Object[3];
            fila[0] = jTextField2.getText();
            fila[1] = jTextField3.getText();
            fila[2] = jTextField4.getText();
            modelo.addRow(fila);
            jTable1.setModel(modelo);
        }
        jTextField2.setText(null);
        jTextField3.setText(null);
        jTextField4.setText(null);
    }//GEN-LAST:event_jButton2ActionPerformed

