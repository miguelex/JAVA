    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Cargamos una nueva transicion y probamos la clonacion
        TransicionAFND transicion = new TransicionAFND();
        ArrayList<Integer> ef = new ArrayList<>();
        transicion.setEstadoInicial(Integer.parseInt(jTextField1.getText()));
        transicion.setCaracter(jTextField2.getText().charAt(0));
        ef.add(Integer.parseInt(jTextField3.getText()));
        transicion.setEstadosFinales(AFND.ListToInt(ef));
        Origen.getTransiciones().add(transicion);
        jTextArea1.setText(Origen.toString());
        jTextArea2.setText(Copia.toString());
    }//GEN-LAST:event_jButton2ActionPerformed

