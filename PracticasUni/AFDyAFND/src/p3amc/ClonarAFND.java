    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Cargamos un archivo de automata, clonamos y mostramos el resultado
        JFileChooser filechooser = new JFileChooser();
        int returnVal = filechooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            Origen = AFND.pedir(filechooser.getSelectedFile().getAbsolutePath());
            Copia = (AFND) Origen.clone();
            jTextArea1.setText(Origen.toString());
            jTextArea2.setText(Copia.toString());
        } else {
            System.out.println("Cancelado por el usuario");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        /// Nos aseguramos de que es un entero
        if (!Character.isDigit(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar())) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_jTextField1KeyTyped

